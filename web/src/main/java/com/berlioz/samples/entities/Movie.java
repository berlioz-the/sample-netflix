package com.berlioz.samples.entities;

public class Movie {
    int id;
    String name;
    String year;
    String poster;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPoster() {
        return poster;
    }

    public String getYear() {
        return year;
    }
}
